export const StatusIndicator: React.FC<React.SVGProps<SVGSVGElement> & { status: 'red' | 'yellow' | 'green' | 'gray' }> = ({ status, ...props }) => {
  let colorClass = "text-gray-500"; // Default (inactive)
  if (status === 'red') colorClass = "text-red-500";
  else if (status === 'yellow') colorClass = "text-yellow-400";
  else if (status === 'green') colorClass = "text-green-500";

  return (
    <svg xmlns="http://www.w3.org/2000/svg" viewBox="0 0 24 24" fill="currentColor" className={`${colorClass} ${props.className || ''}`} {...props}>
      <circle cx="12" cy="12" r="10" />
    </svg>
  );
};
